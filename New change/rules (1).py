def build_rules_catalog() -> Dict:
    """Return a catalog dict compatible with ScannerEngine."""
    from collections import Counter
    cwe_catalog: Dict[str, Dict] = {}
    for rule in ALL_RULES:
        cwe = rule["cwe_id"]
        if cwe not in cwe_catalog:
            cwe_catalog[cwe] = {
                "cwe_name": rule["cwe_name"],
                "samples": 1,
                "source": rule["source"],
            }
        else:
            cwe_catalog[cwe]["samples"] += 1

    return {
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "rule_count": len(ALL_RULES),
        "rules": ALL_RULES,
        "cwe_catalog": cwe_catalog,
    }
